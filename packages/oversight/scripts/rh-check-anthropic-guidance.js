#!/usr/bin/env node
// check-anthropic-guidance.js — fetches Anthropic doc pages, detects content drift.

const fs = require("fs");
const path = require("path");
const crypto = require("crypto");
const https = require("https");
const { config } = require("./lib/config");

const CACHE_PATH = path.join(config.claudeDir, "cache", "anthropic-docs-hashes.json");
const OUT_PATH = path.join(config.oversightDir, "..", "environment", "GUIDANCE_CHANGES.md");

const TARGETS = [
  { id: "hooks",         url: "https://docs.claude.com/en/docs/claude-code/hooks" },
  { id: "settings",      url: "https://docs.claude.com/en/docs/claude-code/settings" },
  { id: "subagents",     url: "https://docs.claude.com/en/docs/claude-code/sub-agents" },
  { id: "skills",        url: "https://docs.claude.com/en/docs/claude-code/skills" },
  { id: "mcp",           url: "https://docs.claude.com/en/docs/claude-code/mcp" },
  { id: "statusline",    url: "https://docs.claude.com/en/docs/claude-code/statusline" },
  { id: "agent-sdk",     url: "https://docs.claude.com/en/api/agent-sdk/overview" },
  { id: "tool-use",      url: "https://docs.claude.com/en/docs/agents-and-tools/tool-use/overview" },
  { id: "models",        url: "https://docs.claude.com/en/docs/about-claude/models/overview" },
];

function fetchOnce(url, timeoutMs = 10000, depth = 0) {
  return new Promise((resolve) => {
    const req = https.get(url, {
      headers: { "User-Agent": "rh-claude-oversight/daily-regen (docs-drift-check)", "Accept": "text/html,application/xhtml+xml" },
      timeout: timeoutMs,
    }, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location && depth < 3) {
        res.resume();
        return resolve(fetchOnce(new URL(res.headers.location, url).toString(), timeoutMs, depth + 1));
      }
      const chunks = [];
      res.on("data", (c) => chunks.push(c));
      res.on("end", () => resolve({ ok: res.statusCode === 200, status: res.statusCode, body: Buffer.concat(chunks).toString("utf8") }));
    });
    req.on("error", (e) => resolve({ ok: false, status: 0, body: "", error: e.message }));
    req.on("timeout", () => { req.destroy(); resolve({ ok: false, status: 0, body: "", error: "timeout" }); });
  });
}

function normalize(html) {
  const nextDataMatch = html.match(/<script\s+id="__NEXT_DATA__"[^>]*>([\s\S]*?)<\/script>/i);
  const nextData = nextDataMatch ? nextDataMatch[1] : "";
  const stripped = html
    .replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, "")
    .replace(/<style\b[^>]*>[\s\S]*?<\/style>/gi, "")
    .replace(/<!--[\s\S]*?-->/g, "")
    .replace(/<meta[^>]*(?:csrf|nonce|build-id|deploy-id)[^>]*>/gi, "")
    .replace(/\s+/g, " ").trim();
  return (stripped + " " + nextData).replace(/"buildId":"[^"]+"/g, '"buildId":"X"');
}

function sha256(s) { return crypto.createHash("sha256").update(s, "utf8").digest("hex"); }

function loadCache() { try { return JSON.parse(fs.readFileSync(CACHE_PATH, "utf8")); } catch { return {}; } }
function saveCache(obj) {
  try { fs.mkdirSync(path.dirname(CACHE_PATH), { recursive: true }); fs.writeFileSync(CACHE_PATH, JSON.stringify(obj, null, 2), "utf8"); }
  catch (e) { process.stderr.write(`cache write failed: ${e.message}\n`); }
}

async function main() {
  const cache = loadCache();
  const now = new Date().toISOString();

  const responses = await Promise.all(TARGETS.map(t => fetchOnce(t.url).then(r => ({ t, r }))));

  const report = [];
  for (const { t, r } of responses) {
    if (!r.ok) { report.push({ ...t, status: r.status, error: r.error || `HTTP ${r.status}`, changed: false }); continue; }
    const newHash = sha256(normalize(r.body));
    const prev = cache[t.id] || {};
    const prevHash = prev.hash || null;
    const first = prevHash === null;
    const changed = !first && prevHash !== newHash;
    report.push({ ...t, status: 200, prevHash, newHash, changed, first });
    cache[t.id] = { hash: newHash, last_checked: now, last_changed: changed ? now : (prev.last_changed || now) };
  }

  saveCache(cache);

  const changedRows = report.filter(r => r.changed);
  const firstRows   = report.filter(r => r.first);
  const errorRows   = report.filter(r => r.error);

  const lines = [
    "# Anthropic Guidance — Drift Check", "",
    `**Last run:** ${now}`,
    `**Changed since last run:** ${changedRows.length} of ${report.length} pages`,
    `**First-time snapshots:** ${firstRows.length} (initial hash seeded — no drift yet)`,
    `**Errors:** ${errorRows.length}`, "",
    `> Auto-generated by \`rh-check-anthropic-guidance.js\` (daily-regen pipeline).`,
    "> A change here means Anthropic shipped something — skim the linked page to decide if the local setup needs adjustment.",
    "> Hashes are over normalized HTML (scripts/styles/comments stripped) — small layout tweaks will NOT trip a false positive.", "",
  ];

  if (changedRows.length > 0) {
    lines.push("## Changed", "", "| Page | URL | Prev hash | New hash | Detected |", "|---|---|---|---|---|");
    for (const r of changedRows) lines.push(`| ${r.id} | <${r.url}> | \`${(r.prevHash || "").slice(0, 12)}\` | \`${r.newHash.slice(0, 12)}\` | ${cache[r.id].last_changed} |`);
    lines.push("");
  }
  if (firstRows.length > 0) {
    lines.push("## First-time snapshots", "");
    for (const r of firstRows) lines.push(`- \`${r.id}\` — <${r.url}> — hash \`${r.newHash.slice(0, 12)}\``);
    lines.push("");
  }
  if (errorRows.length > 0) {
    lines.push("## Errors", "");
    for (const r of errorRows) lines.push(`- \`${r.id}\` — <${r.url}> — ${r.error}`);
    lines.push("");
  }

  lines.push("## All watched pages", "", "| Page | URL | Last checked | Last changed |", "|---|---|---|---|");
  for (const t of TARGETS) { const c = cache[t.id] || {}; lines.push(`| ${t.id} | <${t.url}> | ${c.last_checked || "—"} | ${c.last_changed || "—"} |`); }
  lines.push("");

  try { fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true }); fs.writeFileSync(OUT_PATH, lines.join("\n"), "utf8"); }
  catch (e) { process.stderr.write(`output write failed: ${e.message}\n`); process.exit(2); }

  console.log(`checked ${report.length} pages — ${changedRows.length} changed, ${firstRows.length} first-seen, ${errorRows.length} errors`);
  process.exit(0);
}

main().catch((e) => { process.stderr.write(`unhandled: ${e.message}\n`); process.exit(1); });
