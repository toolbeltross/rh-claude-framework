/**
 * StatusLine wrapper generator.
 *
 * Given the user's original statusLine script path, emit a self-contained
 * wrapper script that runs the original script (preserving its stdout) while
 * also POSTing the parsed stdin JSON to /api/status with _source='statusLineWrapped'.
 *
 * The generated wrapper must:
 *   - Never throw up to Claude Code (all failure paths resolve cleanly)
 *   - Time out the POST at 500ms (never block on telemetry)
 *   - Pass stdin through to the original script unchanged
 *   - Pipe the original's stdout directly to our stdout
 *   - Have '// rh-telemetry:wrapped <ORIGINAL_PATH>' in the first two lines
 *     (so the classifier detects it as telemetry-wrapper)
 */
import { writeFileSync, chmodSync, existsSync } from 'fs';
import { BASE_URL } from '../server/config.js';

/**
 * Build the wrapper script content.
 * @param {string} originalAbsPath - absolute path to the user's original statusLine script
 * @param {string} baseUrl - telemetry server base URL (e.g. http://localhost:7890)
 * @returns {string}
 */
export function buildWrapperSource(originalAbsPath, baseUrl = BASE_URL) {
  // Escape backslashes and single quotes for JS string literals
  const escOriginal = originalAbsPath.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const escBaseUrl = baseUrl.replace(/\\/g, '\\\\').replace(/'/g, "\\'");

  return `#!/usr/bin/env node
// rh-telemetry:wrapped ${originalAbsPath}
// Generated by rh-telemetry. Preserves the original statusLine output while
// forwarding parsed stdin JSON to /api/status so the telemetry dashboard gets
// live context window updates at Claude Code's statusLine render cadence.
// DO NOT edit — re-run 'rh-telemetry repair-statusline' to regenerate.
import http from 'http';
import { spawn } from 'child_process';
import { URL } from 'url';

const ORIGINAL = '${escOriginal}';
const BASE_URL = '${escBaseUrl}';
const POST_TIMEOUT = 500;

function readStdinBuffer() {
  return new Promise((resolve) => {
    const chunks = [];
    let resolved = false;
    const done = () => {
      if (resolved) return;
      resolved = true;
      resolve(Buffer.concat(chunks).toString('utf-8'));
    };
    process.stdin.on('data', (c) => chunks.push(c));
    process.stdin.on('end', done);
    process.stdin.on('error', done);
    // Safety: if stdin never closes, bail after 2s
    setTimeout(done, 2000);
  });
}

function postStatus(payload) {
  return new Promise((resolve) => {
    try {
      const body = JSON.stringify(payload);
      const u = new URL('/api/status', BASE_URL);
      const req = http.request(
        {
          hostname: u.hostname,
          port: u.port,
          path: u.pathname,
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Content-Length': Buffer.byteLength(body),
          },
          timeout: POST_TIMEOUT,
        },
        (res) => { res.resume(); resolve(); }
      );
      req.on('error', () => resolve());
      req.on('timeout', () => { try { req.destroy(); } catch {} resolve(); });
      req.write(body);
      req.end();
    } catch {
      resolve();
    }
  });
}

(async () => {
  const raw = await readStdinBuffer();

  // Fire-and-forget POST — never block on telemetry
  try {
    const parsed = JSON.parse(raw);
    postStatus({ ...parsed, _source: 'statusLineWrapped' });
  } catch { /* malformed JSON — skip POST, still run original */ }

  // Run the user's original statusLine script. Pipe stdin, passthrough stdout/stderr.
  try {
    const child = spawn('node', [ORIGINAL], { stdio: ['pipe', 'inherit', 'inherit'] });
    child.on('error', () => process.exit(0));
    child.on('exit', (code) => process.exit(code || 0));
    child.stdin.on('error', () => {}); // ignore EPIPE if child closes early
    child.stdin.write(raw);
    child.stdin.end();
  } catch {
    process.exit(0);
  }
})();
`;
}

/**
 * Write the wrapper script to disk.
 * @param {string} originalAbsPath - absolute path to the user's statusLine script
 * @param {string} outputPath - where to write the wrapper (e.g. ~/.claude/scripts/statusline-wrapped.js)
 * @param {string} [baseUrl] - override for telemetry base URL
 * @returns {{written: string, markerOk: boolean}}
 */
export function generateWrapper(originalAbsPath, outputPath, baseUrl = BASE_URL) {
  if (!existsSync(originalAbsPath)) {
    throw new Error(`Original statusLine script not found: ${originalAbsPath}`);
  }
  const source = buildWrapperSource(originalAbsPath, baseUrl);
  writeFileSync(outputPath, source, 'utf-8');
  try { chmodSync(outputPath, 0o755); } catch { /* non-POSIX — ignore */ }
  const firstTwoLines = source.split('\n').slice(0, 2).join('\n');
  return {
    written: outputPath,
    markerOk: firstTwoLines.includes('// rh-telemetry:wrapped'),
  };
}
